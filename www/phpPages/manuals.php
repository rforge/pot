We have written a <a href="../docs/guide.pdf">package vignette</a> to
help new users.  This user's guide is a part of the package - just
run <strong>vignette(POT)</strong> once the package is loaded.<br/>
For a quick overview, one can have a look at
the <a href="../docs/RNews_2007-1.pdf"> R News (7):1 (2OO7)</a> article
or by running the univariate and bivariate demos -
i.e. <strong>demo(POT)</strong> and <strong>demo(bvPOT)</strong>.
